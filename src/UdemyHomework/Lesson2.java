package UdemyHomework;

public class Lesson2 {
    public static void main(String[] args) {
        byte n1 = 0b1100;
        byte n2 = 014;
        byte n3 = 12;
        byte n4 = 0xc;
        System.out.println(n1 + "\n" + n2 + "\n" + n3 + "\n" + n4);

        short n5 = -0b10100010100;
        short n6 = -02424;
        short n7 = -1300;
        short n8 = -0x514;
        System.out.println(n5 + "\n" + n6 + "\n" + n7 + "\n" + n8);

        int n9 = 0b0;
        int n10 = 00;
        int n11 = 0;
        int n12 = 0x0;
        System.out.println(n9 + "\n" + n10 + "\n" + n11 + "\n" + n12);

        long n13 = 0b111010110111100110100010101;
        long n14 = 0726746425;
        long n15 = 123456789;
        long n16 = 0x75BCD15;

        System.out.println(n13 + "\n" + n14 + "\n" + n15 + "\n" + n16);

    }
}
